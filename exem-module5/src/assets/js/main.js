function deleteConfirmation(buttonId) {
  const swalWithBootstrapButtons = Swal.mixin({
    customClass: {
      confirmButton: "btn swal2-confirm btn-customer-switch",
      cancelButton: "btn swal2-cancel btn-customer-switch",
    },
    buttonsStyling: false,
  });

  document.getElementById(buttonId).addEventListener("click", function () {
    swalWithBootstrapButtons
        .fire({
          title: "Bạn có muốn xóa không?",
          text: "Bài báo này sẽ bị loại bỏ!",
          icon: "warning",
          showCancelButton: true,
          confirmButtonText: "Xác nhận",
          cancelButtonText: "Hủy",
          reverseButtons: false,
        })
        .then((result) => {
          if (result.isConfirmed) {
            swalWithBootstrapButtons.fire(
                "Đã Xóa!",
                "Bài báo này đã bị loại bỏ.",
                "success"
            );
            // TODO: Call delete API or other delete function
          } else if (result.dismiss === Swal.DismissReason.cancel) {
            swalWithBootstrapButtons.fire(
                "Đã Hủy",
                "Bài báo này vẫn an toàn.",
                "error"
            );
          }
        });
  });
}

const buttonSearch = document.querySelector(".button-search");
const formSearch = document.querySelector(".form-search");

buttonSearch.addEventListener("click", (e) => {
  e.preventDefault();
  formSearch.classList.toggle("active");
});

window.addEventListener("click", (e) => {
  const isOutsideFormSearch = !formSearch.contains(e.target);
  const isOutsideButtonSearch = !buttonSearch.contains(e.target);

  if (formSearch.classList.contains("active") && isOutsideFormSearch && isOutsideButtonSearch) {
    formSearch.classList.remove("active");
  }
}, true);

